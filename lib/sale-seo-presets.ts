export function getSaleSeoPreset(slug: string) {
  return saleSeoPresets.find((preset) => preset.slug === slug);
}

export function applySaleSeoPresetFilter(filters: SaleSearchFilters, preset: SaleSeoPreset): SaleSearchFilters {
  return {
    ...filters,
    filters: [preset.filter],
    page: 1,
  };
}
